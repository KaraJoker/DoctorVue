package com.chero.client.utils;

import lombok.Data;

/**
 * Created by hxh on 2018/5/16.
 */
@Data
public class CustomPageable {
    private Integer pageId;
    private Integer size;
    public Integer getPageId() {
        if (pageId == null) {
            pageId = 1;
        }
        return pageId;
    }
    public Integer getSize() {
        if (size == null) {
            size = 10;
        }
        return size;
    }
}
